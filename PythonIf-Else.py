#!/bin/python3
num = int(input())
acceptable_values = list(range(1, 101))
two_to_five = list(range(2, 6))
six_to_twenty = list(range(6, 21))

if num in acceptable_values:
    #continue with program
    if (num % 2) != 0 :
        print ("Weird")
    elif num in two_to_five:
        print ("Not Weird")
    elif num in six_to_twenty:
        print ("Weird")
    elif num > 20:
        print ("Not Weird")
else:
    print ("Out of range bucko")
    
